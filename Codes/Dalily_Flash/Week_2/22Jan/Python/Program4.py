no1 = int(input("Enter No1 = "))
no2 = int(input("Enter No2 = "))
op = str(input("Enter Operator = "))

if(op=='+'):
    add = no1+no2
    print("Addition of",no1,"and",no2,"=",add)
elif(op=='-'):
    sub = no1-no2
    print("Substraction of",no1,"and",no2,"=",sub)
elif(op=='*'):
    mul = no1*no2
    print("Multiplication of",no1,"and",no2,"=",mul)
elif(op=='/'):
    div = no1*no2
    print("Division of",no1,"and",no2,"=",div)
    
    
